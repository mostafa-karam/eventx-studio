/**
 * seed-revenue-history.js
 *
 * Seeds realistic monthly ticket booking history across the past 12 months
 * so the Revenue Overview graph on /admin/dashboard shows distinct data
 * when switching between "This Year" (12 months) vs "Last 6 Months".
 *
 * ✅ SAFE: Does NOT delete existing real data.
 *         Removes previously history-seeded tickets first, then re-seeds.
 *
 * Usage:
 *   cd backend
 *   node scripts/seed-revenue-history.js
 *
 * Options (env vars):
 *   MONTHS=12  How many months back to seed (default: 12)
 */

const mongoose = require('mongoose');
const dotenv = require('dotenv');
dotenv.config();

const Event  = require('../models/Event');
const Ticket = require('../models/Ticket');
const User   = require('../models/User');

const CONNECTION_URI = process.env.MONGODB_URI || 'mongodb://localhost:27017/eventx';
const MONTHS_BACK    = parseInt(process.env.MONTHS || '12', 10);

// Natural growth curve: older months have fewer bookings/revenue
// 12 entries: index 0 = most-distant month, index 11 = most-recent month
const MONTHLY_TICKETS = [8, 10, 12, 15, 18, 20, 22, 28, 30, 32, 38, 45];

function randomDateInMonth(year, month) {
  const start = new Date(year, month, 1);
  const end   = new Date(year, month + 1, 0, 23, 59, 59);
  return new Date(start.getTime() + Math.random() * (end.getTime() - start.getTime()));
}

const pick = (arr) => arr[Math.floor(Math.random() * arr.length)];

async function seedRevenueHistory() {
  console.log('\n🌱 Revenue History Seeder Started');
  console.log(`   Seeding ${MONTHS_BACK} months of booking history...\n`);

  await mongoose.connect(CONNECTION_URI);
  console.log('✅ Connected to MongoDB');

  // Remove previously seeded history tickets (identified by note field)
  const removed = await Ticket.deleteMany({ notes: 'history-seed' });
  if (removed.deletedCount > 0) {
    console.log(`🗑  Removed ${removed.deletedCount} old history-seed tickets\n`);
  }

  // Load existing events and users
  const events = await Event.find({ status: { $in: ['published', 'completed'] } })
    .select('_id pricing seating');
  const users = await User.find({ role: 'user' }).select('_id').limit(100);

  if (events.length === 0) {
    console.error('❌ No published/completed events found. Run seed-all.js first.');
    process.exit(1);
  }
  if (users.length === 0) {
    console.error('❌ No regular users found. Run seed-all.js first.');
    process.exit(1);
  }

  console.log(`   Found ${events.length} events and ${users.length} users to attach tickets to.\n`);

  const now = new Date();
  let totalTickets = 0;
  let totalRevenue = 0;

  // Loop from 12 months ago → 1 month ago (skip current month)
  for (let m = MONTHS_BACK - 1; m >= 1; m--) {
    const curveIndex  = MONTHS_BACK - 1 - m;   // 0 = most distant
    const baseCount   = MONTHLY_TICKETS[Math.min(curveIndex, MONTHLY_TICKETS.length - 1)];
    const jitter      = 0.8 + Math.random() * 0.4;  // ±20% randomness
    const ticketCount = Math.max(3, Math.round(baseCount * jitter));

    // Target month
    const targetDate  = new Date(now.getFullYear(), now.getMonth() - m, 1);
    const targetYear  = targetDate.getFullYear();
    const targetMonth = targetDate.getMonth(); // 0-indexed

    const docs = [];
    let monthRevenue = 0;

    for (let t = 0; t < ticketCount; t++) {
      const event   = pick(events);
      const user    = pick(users);
      const amount  = event.pricing?.type === 'paid' ? (event.pricing?.amount || 0) : 0;
      const bDate   = randomDateInMonth(targetYear, targetMonth);
      const seatNum = `HS${String(curveIndex).padStart(2,'0')}${String(t).padStart(3,'0')}`;

      docs.push({
        event:       event._id,
        user:        user._id,
        seatNumber:  seatNum,
        bookingDate: bDate,             // ← the field analytics groups by
        status:      'used',
        notes:       'history-seed',    // marker to allow clean re-seeding
        payment: {
          status:        'completed',
          amount,
          currency:      event.pricing?.currency || 'USD',
          paymentMethod: amount > 0 ? 'credit_card' : 'free',
          paymentDate:   bDate,
        },
        metadata: {
          source: 'api',                // ← valid enum value
        },
      });

      monthRevenue += amount;
    }

    // Use insertMany with ordered:false so one failure doesn't abort the batch.
    // qrCode is generated by the pre-validate hook automatically.
    await Ticket.insertMany(docs, { ordered: false });

    totalTickets += ticketCount;
    totalRevenue += monthRevenue;

    const monthLabel = targetDate.toLocaleString('en-US', { month: 'long', year: 'numeric' });
    const boundary   = m === Math.ceil(MONTHS_BACK / 2) ? '  ← 6-month boundary' : '';
    console.log(`   📅 ${monthLabel.padEnd(18)} | 🎟  ${String(ticketCount).padStart(3)} tickets | 💵 $${String(monthRevenue).padStart(6)}${boundary}`);
  }

  console.log('\n' + '─'.repeat(58));
  console.log(`   Total tickets inserted : ${totalTickets}`);
  console.log(`   Total revenue seeded   : $${totalRevenue.toLocaleString()}`);
  console.log('─'.repeat(58));
  console.log('\n✅ Done! Open /admin/dashboard and switch between:');
  console.log('     "Last 6 Months" → bottom half of the chart');
  console.log('     "This Year"     → all months, shows growth trend\n');

  process.exit(0);
}

seedRevenueHistory().catch((err) => {
  console.error('❌ Seeding failed:', err.message);
  process.exit(1);
});
